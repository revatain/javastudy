package ch03;
public class CompareTest01 {
	public static void main(String[] args) {
		int x = 10;
		int y = 2;
		System.out.println(x==y); //x와 y는 같은가?
		System.out.println(x!=y); //x와 y는 다른가?
		System.out.println(x>y);  //x는 y보다 큰가?
		System.out.println(x<y);  //x는 y보다 작은가?
		System.out.println(x>=y); //x는 y보다 크거나 같은가?
		System.out.println(x<=y); //x는 y보다 작거나 같은가?
	}
}
